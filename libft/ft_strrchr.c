#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t	len;

	len = ft_strlen(s);
	if (c == '\0')
		return ((char *)&s[len]);
	s += len;
	while (len--)
	{
		if (*--s == (char)c)
			return ((char *)s);
	}
	return (NULL);
}
